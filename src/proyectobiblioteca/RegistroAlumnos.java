    private void tablaalumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaalumnosMouseClicked
        // TODO add your handling code here:
        int fila=tablaalumnos.getSelectedRow();
        idalumnos.setText(tablaalumnos.getValueAt(fila, 0).toString());
        rut.setText(tablaalumnos.getValueAt(fila, 1).toString());
        dv.setText(tablaalumnos.getValueAt(fila, 2).toString());
        nombrealumno.setText(tablaalumnos.getValueAt(fila, 3).toString());
        apellidopat.setText(tablaalumnos.getValueAt(fila, 4).toString());
        apellidomat.setText(tablaalumnos.getValueAt(fila, 5).toString());
        numeromatriculas.setText(tablaalumnos.getValueAt(fila, 6).toString());
        
        idalumnos.setEditable(false);
        
    }//GEN-LAST:event_tablaalumnosMouseClicked

