    private void registraralumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registraralumnoActionPerformed
        // TODO add your handling code here:
        
        Connection conexion=conectar.abrirConexion();
        
        datosalumno.AgregarAlumno(nombrealumno, apellidopat, apellidomat, rut, dv, numeromatriculas, conexion);
        
        String sql= "select * from alumno";
        ResultSet result=conectar.Consulta(conexion, sql);
        metodosadicionales.MostradDB(result, tablaalumnos);
        conectar.cerrarConexion(conexion);
    }//GEN-LAST:event_registraralumnoActionPerformed

